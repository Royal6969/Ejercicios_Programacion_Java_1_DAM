package Ejer_12_NumerosRepetidos;

public class Entero {

	private int num;

	public Entero(int num) {
		super();
		this.num = num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
	
	public String toString() {
		return num+" ";
	}
}