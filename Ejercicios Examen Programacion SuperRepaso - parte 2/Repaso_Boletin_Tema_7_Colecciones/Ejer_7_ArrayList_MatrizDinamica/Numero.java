package Ejer_7_ArrayList_MatrizDinamica;

public class Numero {

	int num;

	public Numero() {
		super();
	}

	public Numero(int num) {
		super();
		this.num = num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	@Override
	public String toString() {
		return "Numero [num=" + num + "]";
	}
	
	
}
